    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        String nombre = campoNombre.getText();
        String DNI = campoDNI.getText();
        String edad = campoEdad.getText();
        String direccion = campoDireccion.getText();
        String distrito = campoDistrito.getText();
        String celular =  campoCelular.getText();
        String codigoAlumno = campoCodigo.getText();
        String modalidad = campoModalidad.getText();
        String carrera = campoCarrera.getText();
        for(Alumno alumno:Contenedor.getAlumnos()){
            if(alumno.getCodigoAlumno().equals(codigoAlumno)){
                campo5.setText("EL ALUMNO YA EXISTE");
                return;
            }
        }
        Alumno alumno = new Alumno(nombre,DNI,edad,direccion,distrito,celular,codigoAlumno,modalidad,carrera);
        Contenedor.getAlumnos().add(alumno);
        campo5.setText("EL ALUMNO REGISTRADO");
        
    }//GEN-LAST:event_botonAgregarActionPerformed

