    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        String nombre = campoNombre.getText();
        String DNI = campoDNI.getText();
        String edad = campoEdad.getText();
        String direccion = campoDireccion.getText();
        String distrito = campoDistrito.getText();
        String celular =  campoCelular.getText();
        String codigoAlumno = campoDNI.getText();
        String modalidad = campoModalidad.getText();
        String carrera = campoCarrera.getText();
        for (int i=0; i<Contenedor.getAlumnos().size(); i++) {
            Alumno alumno = Contenedor.getAlumnos().get(i);
            if (alumno.getNombre().equals(nombre)
                    && alumno.getDocumentoIdentidad().equals(DNI)
                    && alumno.getEdad().equals(edad)
                    && alumno.getDireccion().equals(direccion)
                    && alumno.getDistrito().equals(distrito)
                    && alumno.getCelular().equals(celular)
                    && alumno.getNombre().equals(nombre)
                    && alumno.getModalidadIngreso().equals(modalidad)
                    && alumno.getCarreraProfesional().equals(carrera)) {
                Contenedor.getAlumnos().remove(i);
                campo5.setText("ALUMNO ELIMINADO");
                return;
            }
        }
        campo5.setText("NO EXISTE EL ALUMNO");
    }//GEN-LAST:event_botonEliminarActionPerformed

    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        String nombre = campoNombre.getText();
        String DNI = campoDNI.getText();
        String edad = campoEdad.getText();
        String direccion = campoDireccion.getText();
        String distrito = campoDistrito.getText();
        String celular =  campoCelular.getText();
        String codigoAlumno = campoDNI.getText();
        String modalidad = campoModalidad.getText();
        String carrera = campoCarrera.getText();
        
        for(Alumno alumno:Contenedor.getAlumnos()){
            if(alumno.getCodigoAlumno().equals(codigoAlumno)){
                if(!campoNombre.getText().equals(""))alumno.setNombre(nombre);
                if(!campoDNI.getText().equals(""))alumno.setDocumentoIdentidad(DNI);
                if(!campoEdad.getText().equals(""))alumno.setEdad(edad);
                if(!campoDireccion.getText().equals(""))alumno.setDireccion(direccion);
                if(!campoDistrito.getText().equals(""))alumno.setDistrito(distrito);
                if(!campoCelular.getText().equals(""))alumno.setCelular(celular);
                if(!campoModalidad.getText().equals(""))alumno.setModalidadIngreso(modalidad);
                if(!campoCarrera.getText().equals(""))alumno.setCarreraProfesional(carrera);
                campo5.setText("ALUMNO MODIFICADO");
                return;
            }
        }
        campo5.setText("NO EXISTE EL ALUMNO A MODIFICAR");
        
    }//GEN-LAST:event_botonModificarActionPerformed

