    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        String codigoCurso = campo1.getText();
        String nombreCurso = campo2.getText();
        String creditos = campo3.getText();
        String categoria = campo4.getText();
        for(Curso curso:Contenedor.getCursos()){
            if(curso.getCodigoCurso().equals(codigoCurso)){
                campo5.setText("EL CURSO YA ESTA REGISTRADO");
                return;
            }
        }
        Curso curso = new Curso(codigoCurso,nombreCurso,creditos,categoria);
        Contenedor.getCursos().add(curso);
        campo5.setText("CURSO REGISTRADO");
        
    }//GEN-LAST:event_botonAgregarActionPerformed

