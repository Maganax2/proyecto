    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        String codigoCurso = campo1.getText();
        String nombre = campo2.getText();
        String creditos = campo3.getText();
        String categoria = campo4.getText();
        for (int i=0; i<Contenedor.getAlumnos().size(); i++) {
            Curso curso = Contenedor.getCursos().get(i);
            if (curso.getCodigoCurso().equals(codigoCurso)
                    && curso.getNombre().equals(nombre)
                    && curso.getCreditos().equals(creditos)
                    && curso.getCategoria().equals(categoria)) {
                Contenedor.getAlumnos().remove(i);
                campo5.setText("CURSO REMOVIDO");
                return;
            }
        }
        campo5.setText("NO EXISTE DICHO CURSO");
    }//GEN-LAST:event_botonEliminarActionPerformed

    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        String codigoCurso = campo1.getText();
        String nombre = campo2.getText();
        String creditos = campo3.getText();
        String categoria = campo4.getText();
        
        for(Curso curso:Contenedor.getCursos()){
            if(curso.getCodigoCurso().equals(codigoCurso)){
                if(!campo2.getText().equals(""))curso.setNombre(nombre);
                if(!campo3.getText().equals(""))curso.setCreditos(creditos);
                if(!campo4.getText().equals(""))curso.setCategoria(categoria);
                campo5.setText("CURSO MODIFICADO");
                return;
            }
        }
        campo5.setText("NO EXISTE DICHO CURSO A MODIFICAR");
        
    }//GEN-LAST:event_botonModificarActionPerformed

